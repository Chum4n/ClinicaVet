private void inserirCategoria() {
            String sql = "INSERT INTO CATEGORIA VALUES(null,?)";
            try{
            conn = Conexao.obterConexao();
            stm = conn.prepareStatement(sql);
            stm.setString(1, txtNomeC.getText());
            stm.execute();
            
            JOptionPane.showMessageDialog(null,"CATEGORIA INSERIDA COM SUCESSO");
            this.setVisible(false);
            }
            catch(Exception erro){
            JOptionPane.showMessageDialog(this,
                         erro.getMessage());
            }
            finally{
                   Conexao.fecharConexao(conn, stm);
                }
       }

